package com.metanit.draconcave;

public class HiddenTreasures {
    private String name;
    private double cost;

    public HiddenTreasures(String name, double cost) {
        this.name = name;
        this.cost = cost;
    }

    public String getName() {
        return name;
    }

    public double getCost() {
        return cost;
    }
}
